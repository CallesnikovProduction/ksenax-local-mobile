package com.kolesnikovprod.ksetaorch.communication.tools.contracts

/**
 * Базовый уровень риска tool-а или конкретного tool call-а.
 *
 * Risk level помогает prompt-слою объяснить модели цену действия, а policy/UI
 * слоям решить, нужен ли отказ, уточнение или подтверждение пользователя.
 *
 * @since 0.2
 * @author Stephan Kolesnikov
 */
enum class KsenaxToolRiskLevel {

    /**
     * Низкий риск: действие обычно обратимо или почти не влияет на данные
     * пользователя.
     *
     * @since 0.2
     */
    LOW,

    /**
     * Средний риск: действие может изменить состояние приложения или потребовать
     * явного пользовательского понимания.
     *
     * @since 0.2
     */
    MEDIUM,

    /**
     * Высокий риск: действие может затронуть данные, приватность, деньги,
     * безопасность или необратимые операции.
     *
     * @since 0.2
     */
    HIGH,
}
