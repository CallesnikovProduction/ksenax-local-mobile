package com.kolesnikovprod.ksetaorch.download

/**
 * Минимальный контракт компонента, который ставит модельный файл в очередь загрузки.
 *
 * Интерфейс намеренно не знает про конкретную модель, UI-состояние, проверку SHA256
 * и запуск LiteRT-LM. Его задача - принять адрес удаленного файла и локальные
 * идентификаторы модели, передать загрузку нижнему слою и вернуть id созданной задачи.
 *
 * Реализация может использовать Android DownloadManager, собственный HTTP-клиент или
 * другой механизм доставки файла. Для текущего Android-прототипа реализация через
 * DownloadManager удобна тем, что загрузка может продолжаться вне активной Activity,
 * а прогресс и статус можно потом восстановить по возвращенному download id.
 *
 * @since 0.1
 * @author Stepan Kolesnikov
 * @see KsenaxDownloadWrapper
 * @see KsenaxModelDownloader
 */
fun interface KsenaxDownloader {

    /**
     * Ставит файл модели в очередь загрузки.
     *
     * Метод не должен считать модель установленной сразу после вызова. Возвращенный id
     * означает только то, что задача загрузки создана. После завершения загрузки внешний
     * слой обязан отдельно проверить файл: существование, размер, SHA256 и, позднее,
     * возможность открыть модель через LiteRT-LM.
     *
     * @param url удаленный адрес файла модели.
     * @param fileName имя локального файла, под которым модель будет сохранена.
     * @param modelName стабильный идентификатор модели, обычно используется как часть
     * локального пути хранения.
     * @return id задачи загрузки. Для Android DownloadManager это download id, по которому
     * можно позже запросить статус, прогресс или отменить загрузку.
     * @since 0.1
     * @author Stepan Kolesnikov
     */
    fun download(
        url: String,
        fileName: String,
        modelName: String
    ): Long
}
